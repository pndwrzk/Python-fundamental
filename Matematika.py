# operasi perhitungan

# + : penjumlahan
# - : pengurangan
# * : perkalian
# / : pembagian
# ** : perpangkatan
# % : sisa pembagian

x = 10
y = 2

# 10 dikali 10
print(x + y)
print(x - y)
print(x * y)
print(x / y)  # ouput menjadi float
print(x // y)  # output menjadi bilangan bulat
print(x ** y)
print(x % y)
